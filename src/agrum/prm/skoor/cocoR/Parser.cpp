void Parser::SynErr(int n) {
	if (errDist >= minErrDist) SynErr(scanner->filename(),la->line, la->col, n);
	errDist = 0;
}


const ErrorsContainer& Parser::errors(void) const {
	return __errors;
}

void Parser::Get() {
	for (;;) {
		t = la;
		la = scanner->Scan();
		if (la->kind <= maxT) { ++errDist; break; }

		if (dummyToken != t) {
			dummyToken->kind = t->kind;
			dummyToken->pos = t->pos;
			dummyToken->col = t->col;
			dummyToken->line = t->line;
			dummyToken->next = NULL;
			coco_string_delete(dummyToken->val);
			dummyToken->val = coco_string_create(t->val);
			t = dummyToken;
		}
		la = t;
	}
}

void Parser::Expect(int n) {
	if (la->kind==n) Get(); else { SynErr(n); }
}

void Parser::ExpectWeak(int n, int follow) {
	if (la->kind == n) Get();
	else {
		SynErr(n);
		while (!StartOf(follow)) Get();
	}
}

bool Parser::WeakSeparator(int n, int syFol, int repFol) {
	if (la->kind == n) {Get(); return true;}
	else if (StartOf(repFol)) {return false;}
	else {
		SynErr(n);
		while (!(StartOf(syFol) || StartOf(repFol) || StartOf(0))) {
			Get();
		}
		return StartOf(syFol);
	}
}

void Parser::skoor() {
		std::string s, alias; __currentSession = 0; 
		if (StartOf(1)) {
			if (la->kind == 5) {
				Get();
				Ident(s);
				__context->setPackage( s ); 
				while (!(la->kind == 0 || la->kind == 15)) {SynErr(29); Get();}
				Expect(15);
			}
			while (la->kind == 6) {
				Get();
				Ident(s);
				alias = ""; 
				if (la->kind == 12) {
					Get();
					if (la->kind == 13) {
						Get();
						alias = "default"; 
					} else if (la->kind == 3) {
						Get();
						alias = gum::narrow(t->val); 
					} else SynErr(30);
				}
				__context->addImport( t->line, s, alias ); 
				while (!(la->kind == 0 || la->kind == 15)) {SynErr(31); Get();}
				Expect(15);
			}
			while (la->kind == 7) {
				RequestBloc();
			}
		} else if (StartOf(2)) {
			__currentSession = new SkoorSession("default");
			__context->addSession( __currentSession );
			
			Command();
		} else SynErr(32);
}

void Parser::Ident(std::string& s) {
		std::stringstream sBuff; 
		Expect(3);
		sBuff << narrow(t->val); 
		while (la->kind == 25) {
			Get();
			Expect(3);
			sBuff << "." << narrow(t->val); 
		}
		s = sBuff.str(); 
}

void Parser::RequestBloc() {
		Expect(7);
		Expect(3);
		__currentSession = new SkoorSession(gum::narrow(t->val)); 
		Expect(16);
		while (StartOf(2)) {
			Command();
		}
		while (!(la->kind == 0 || la->kind == 17)) {SynErr(33); Get();}
		Expect(17);
		__context->addSession( __currentSession ); __currentSession = 0; 
}

void Parser::Command() {
		if (la->kind == 3) {
			Observe();
		} else if (la->kind == 9) {
			Unobserve();
		} else if (la->kind == 8) {
			Query();
		} else if (la->kind == 10) {
			SetEngine();
		} else if (la->kind == 11) {
			SetGrdEngine();
		} else SynErr(34);
}

void Parser::Observe() {
		std::string left_value, right_value; 
		IdentArray(left_value);
		Expect(18);
		Expect(3);
		right_value = gum::narrow(t->val); 
		Expect(15);
		__currentSession->addObserve(t->line, left_value, right_value); 
}

